/**
 * Удаляет хвосты WordPress из sitemaps и HTML:
 * - Yoast SEO, W3 Total Cache комментарии
 * - Скрипты yandex.st (файла нет), recaptcha (внешний)
 * - https -> оставляем: внешние ссылки (google, ucoz) — относительные для внутренних
 */
const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const EXCLUDE = ['node_modules', '.git', 'scripts', 'tools'];

const WP_COMMENT_RE = /<!-- XML Sitemap generated by Yoast SEO -->\s*\n?|<!--[\s\S]*?W3 Total Cache[\s\S]*?-->\s*\n?|<!--[\s\S]*?Served from:[\s\S]*?W3 Total Cache\s*-->\s*\n?/g;
// yandex.st — путь /yandex.st/ на сервере даёт 404, виджет не работает
const YANDEX_ST_RE = /<script[^>]*src="\/yandex\.st\/[^"]*"[^>]*><\/script>\s*\n?/gi;

// Sitemaps
['sitemap_index.xml', 'post-sitemap.xml', 'post-sitemap2.xml', 'page-sitemap.xml', 'category-sitemap.xml', 'author-sitemap.xml'].forEach(name => {
  const p = path.join(ROOT, name);
  if (!fs.existsSync(p)) return;
  let s = fs.readFileSync(p, 'utf8');
  s = s.replace(WP_COMMENT_RE, '').replace(/\n{3,}/g, '\n\n').trimEnd();
  fs.writeFileSync(p, s);
  console.log('Cleaned sitemap:', name);
});

// HTML: yandex.st и recaptcha
function walk(dir, cb) {
  try {
    for (const e of fs.readdirSync(dir, { withFileTypes: true })) {
      const full = path.join(dir, e.name);
      if (EXCLUDE.some(x => full.includes(path.sep + x + path.sep))) continue;
      if (e.isDirectory()) walk(full, cb);
      else if (e.name.toLowerCase().endsWith('.html')) cb(full);
    }
  } catch (_) {}
}

let htmlCount = 0;
walk(ROOT, (file) => {
  let s = fs.readFileSync(file, 'utf8');
  const orig = s;
  s = s.replace(YANDEX_ST_RE, '');
  if (s !== orig) {
    fs.writeFileSync(file, s);
    htmlCount++;
  }
});
console.log('Removed yandex.st from', htmlCount, 'HTML files.');
